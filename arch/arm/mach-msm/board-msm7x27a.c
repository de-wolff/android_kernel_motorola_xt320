static int bluetooth_power(int on)
{
	int pin, rc;
	int gpio_108 = 108;

	printk("%s: on = %d\n", __func__, on);

	rc = gpio_request(gpio_108, "bt_reset");
	if (rc) {
		printk("failed to request gpio bt_reset\n");
		return -1;
	}
	gpio_direction_output(gpio_108, 1);

	if (on) {
		for (pin = 0; pin < ARRAY_SIZE(bt_config_power_on); pin++) {
			rc = gpio_tlmm_config(bt_config_power_on[pin], 0);
			if (rc) {
				printk(KERN_ERR
				       "%s: gpio_tlmm_config(%#x)=%d\n",
				       __func__, bt_config_power_on[pin], rc);
				return -EIO;
			}
		}

		gpio_set_value(gpio_108, 0);
		mdelay(100);
		gpio_set_value(gpio_108, 1);

	} else {
		for (pin = 0; pin < ARRAY_SIZE(bt_config_power_off); pin++) 
			{			
			rc = gpio_tlmm_config(bt_config_power_off[pin], 0);			
			if (rc) {				
				printk(KERN_ERR"%s: gpio_tlmm_config(%#x)=%d\n",				      
					__func__, bt_config_power_off[pin], rc);				
				return -EIO;			
				}		
			}
		gpio_set_value(gpio_108, 0);
	}

	gpio_free(gpio_108);

	return 0;
}

