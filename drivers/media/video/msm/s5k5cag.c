static int32_t s5k5cag_reg_config(int update_type, int mode)
{
    long rc = 0;
    if (update_type == REG_INIT) {
        return 0;
    } else {
        if (mode == RES_PREVIEW) {
            if (!g_sensor_init_flag) {
                s5k5cag_sensor_wakeup();		
                rc = s5k5cag_i2c_write_table(&s5k5cag_regs.config_preview[0], s5k5cag_regs.config_preview_size);
                if (rc < 0) {
                    PRINTK("Preview fail\n");
                    goto config_fail;
                }
                g_sensor_init_flag = true;
            } else {
                rc = s5k5cag_i2c_write_table(&s5k5cag_regs.config_preview[0], s5k5cag_regs.config_preview_size);
                if (rc < 0) {
                    PRINTK("Preview fail\n");
                    goto config_fail;
                }
            }
	    msleep(250);
        } else {
            CDBG("CAPTURE\n");
            rc = s5k5cag_i2c_write_table(&s5k5cag_regs.config_capture[0], s5k5cag_regs.config_capture_size);
            if (rc < 0) {
                PRINTK("Capture fail\n");
                goto config_fail;
            }
        }
    }
    return rc;
config_fail:
    PRINTK("failed\n");
    return rc;
}

static void csi_config(void)
{
    struct msm_camera_csi_params csi_params;
    if (!CSI_CONFIG) {
        CDBG("\n");
        msm_camio_vfe_clk_rate_set(192000000);
        csi_params.lane_cnt = 1;
        csi_params.data_format = CSI_8BIT;
        csi_params.lane_assign = 0xe4;
        csi_params.dpcm_scheme = 0;
        csi_params.settle_cnt = g_settle_cnt;
        msm_camio_csi_config(&csi_params);
        msleep(20);
        CSI_CONFIG = true;
    }
}

