/************************* I2C function **************************/
static int32_t s5k5cag_i2c_txdata(unsigned short saddr,
        unsigned char *txdata, int length)
{
    struct i2c_msg msg[] = {
        {
            .addr = saddr,
            .flags = 0,
            .len = length,
            .buf = txdata,
        },
    };

#if SENSOR_DEBUG
    if (length == 2)
        CDBG("msm_io_i2c_w: 0x%04x 0x%04x\n",
                *(u16 *) txdata, *(u16 *) (txdata + 2));
    else if (length == 4)
        CDBG("msm_io_i2c_w: 0x%04x\n", *(u16 *) txdata);
    else
        CDBG("msm_io_i2c_w: length = %d\n", length);
#endif
    if (i2c_transfer(s5k5cag_client->adapter, msg, 1) < 0) {
        PRINTK("s5k5cag_i2c_txdata failed\n");
        return -EIO;
    }

    return 0;
}

static int32_t s5k5cag_i2c_write(unsigned short saddr,
        unsigned short waddr, unsigned short wdata, enum s5k5cag_width width)
{
    int32_t rc = -EIO;
    unsigned char buf[4];

    memset(buf, 0, sizeof(buf));
    switch (width) {
        case WORD_LEN: {
                           buf[0] = (waddr & 0xFF00)>>8;
                           buf[1] = (waddr & 0x00FF);
                           buf[2] = (wdata & 0xFF00)>>8;
                           buf[3] = (wdata & 0x00FF);

                           rc = s5k5cag_i2c_txdata(saddr, buf, 4);
                       }
                       break;

        case BYTE_LEN: {
                           buf[0] = waddr;
                           buf[1] = wdata;
                           rc = s5k5cag_i2c_txdata(saddr, buf, 2);
                       }
                       break;

        default:
                       break;
    }

    if (rc < 0)
        PRINTK("i2c_write failed, addr = 0x%x, val = 0x%x!\n",waddr, wdata);

    return rc;
}

static int32_t s5k5cag_i2c_write_table(
        struct s5k5cag_i2c_reg_conf const *reg_conf_tbl,
        int num_of_items_in_table)
{
    int i;
    int32_t rc = -EIO;

    for (i = 0; i < num_of_items_in_table; i++) {
        rc = s5k5cag_i2c_write(s5k5cag_client->addr,
                reg_conf_tbl->waddr, reg_conf_tbl->wdata,
                reg_conf_tbl->width);
        if (rc < 0)
            break;
        if (reg_conf_tbl->mdelay_time != 0)
            mdelay(reg_conf_tbl->mdelay_time);
        reg_conf_tbl++;
    }

    return rc;
}

static int s5k5cag_i2c_rxdata(unsigned short saddr,
        unsigned char *rxdata, int length)
{
    struct i2c_msg msgs[] = {
        {
            .addr   = saddr,
            .flags = 0,
            .len   = 2,
            .buf   = rxdata,
        },
        {
            .addr   = saddr,
            .flags = I2C_M_RD,
            .len   = length,
            .buf   = rxdata,
        },
    };

#if SENSOR_DEBUG
    if (length == 2)
        CDBG("msm_io_i2c_r: 0x%04x 0x%04x\n",
                *(u16 *) rxdata, *(u16 *) (rxdata + 2));
    else if (length == 4)
        CDBG("msm_io_i2c_r: 0x%04x\n", *(u16 *) rxdata);
    else
        CDBG("msm_io_i2c_r: length = %d\n", length);
#endif

    if (i2c_transfer(s5k5cag_client->adapter, msgs, 2) < 0) {
        PRINTK("s5k5cag_i2c_rxdata failed!\n");
        return -EIO;
    }

    return 0;
}

static int32_t s5k5cag_i2c_read(unsigned short   saddr,
        unsigned short raddr, unsigned short *rdata, enum s5k5cag_width width)
{
    int32_t rc = 0;
    unsigned char buf[4];

    if (!rdata)
        return -EIO;

    memset(buf, 0, sizeof(buf));

    switch (width) {
        case WORD_LEN: {
                           buf[0] = (raddr & 0xFF00)>>8;
                           buf[1] = (raddr & 0x00FF);

                           rc = s5k5cag_i2c_rxdata(saddr, buf, 2);
                           if (rc < 0)
                               return rc;

                           *rdata = buf[0] << 8 | buf[1];
                       }
                       break;

        default:
                       break;
    }

    if (rc < 0)
        PRINTK("s5k5cag_i2c_read failed!\n");

    return rc;
}

/******************** Flash control function *********************/
#define FIRE_LUMA      30

#define ctrl_to_current_driver(ctrlp)   \
    (&((ctrlp)->sensordata->flash_data->flash_src->_fsrc.current_driver_src)) 

/*
 * check the current luma, judging should the flash be fire or not
 *
 *  return 1 for firing flash
 *
 *  return 0 for not firing
 */
static int32_t s5k5cag_check_luma_and_not_fire_flash(void)
{
    unsigned short luma = 0;

    CDBG ("s5k5cag: check luma.\n");
    if (s5k5cag_i2c_write(s5k5cag_client->addr, 0xFCFC, 0x7000, WORD_LEN) 
            || s5k5cag_i2c_read(s5k5cag_client->addr, 0x04E8, &luma, WORD_LEN)
            || s5k5cag_i2c_write(s5k5cag_client->addr, 0xFCFC, 0xD000, WORD_LEN))
        PRINTK ("s5k5cag: read i2c luma failed.\n"); 

    CDBG ("s5k5cag: luma = %d\n", luma);

    if (luma > FIRE_LUMA)
        return 1;

    return 0;
}


